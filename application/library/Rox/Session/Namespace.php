/** Zend_Session_Namespace */
require_once 'Zend/Session/Namespace.php';

/**
 * @category    Gonium
 * @package     Rox_Session
 * @author      {@link http://blog.gon.cl/cat/zf Gonzalo Diaz Cruz}
 * @license     http://www.gnu.org/licenses/old-licenses/gpl-2.0.html GNU/GPL v2
 * @copyright   2008 {@link http://labs.gon.cl/gonium Gonzalo Diaz Cruz}
 * @version     $Id$
 */
class Rox_Session_Namespace extends Zend_Session_Namespace implements Countable
{
    /**
    * Get the number of elements stored in the session namespace
    */
    public function count()
    {
        return count( $_SESSION[$this->_namespace] );
    }

    public function toArray()
    {
        return $_SESSION[$this->_namespace];
    }
}
